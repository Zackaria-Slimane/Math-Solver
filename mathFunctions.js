export function sum(x, y) {
	return x + y;
}
export function minus(x, y) {
	return x - y;
}

export function multiply(x, y) {
	return x * y;
}

export function divide(x, y) {
	return x / y;
}

export function power(a, b) {
	return Math.pow(a, b);
}

export function square(a, b) {
	return Math.sqrt(a, b);
}
